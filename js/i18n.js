function applyLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // Texto del ciclo animado
  var cycleEl = document.getElementById('text-animation');
  if (cycleEl && t.hero_cycle) {
    cycleEl.textContent = t.hero_cycle;
    if (typeof $.fn.cycleText === 'function') {
      $(cycleEl).cycleText();
    }
  }

  // Actualiza atributos de lang
  document.documentElement.lang = lang;
  localStorage.setItem('aceaLang', lang);

  // Marca el botón activo
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

document.addEventListener('DOMContentLoaded', function() {
  var saved = localStorage.getItem('aceaLang') || 'es';
  applyLanguage(saved);

  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      applyLanguage(this.getAttribute('data-lang'));
    });
  });
});
